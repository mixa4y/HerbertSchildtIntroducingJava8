package TheBook.Chapter2;

// Demonstrate escape sequences in strings.
// Демонстрация УПРАВЛЯЮЩИХ ПОСЛЕДОВАТЕЛЬНОСТЕЙ в символьных строках

public class List_6_StrDemo {
    public static void main(String[] args){
        System.out.println("First line\nSecond line"); // Использование последовательности \n для вставки символа перевода строки
        System.out.println("A\tB\tC"); // Использование табуляции
        System.out.println("D\tE\tF"); // Для выравнивания вывода
    }
}
